    private void loadButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loadButtonActionPerformed
        // TODO add your handling code here:
        //get names of save files: name,time
        RPGSystem.removeGUI(this);
        String fileName = (String) saveComboBox.getSelectedItem();

        if (fileName != null) {
            fileName = "saves/" + fileName.substring(0, fileName.indexOf(",")) + "/";

            try (FileInputStream fis = new FileInputStream(fileName);
                    ObjectInputStream ois = new ObjectInputStream(fis)) {
                RPGSystem.setActiveCharacter((Character) ois.readObject());
            } catch (Exception ex) {
                System.out.println("Failed to load character\n"
                        + fileName);
            }

            DungeonGUI dungeonMenu = new DungeonGUI();
            RPGSystem.addGUI(dungeonMenu);
            dungeonMenu.setVisible(true);
            dungeonMenu.setLocationRelativeTo(null);
            dispose();
        }

    }//GEN-LAST:event_loadButtonActionPerformed

