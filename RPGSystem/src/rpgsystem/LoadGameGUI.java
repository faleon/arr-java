    public LoadGameGUI() {
        initComponents();

        //populate combo box with save files
        File savesFolder = new File("saves");
        File[] saveList = savesFolder.listFiles(new FilenameFilter() {
            public boolean accept(File directory, String fileName) {
                return fileName.endsWith(".sav");
            }
        });
        if (saveList.length > 0) {
            String[] model = new String[saveList.length];
            SimpleDateFormat sdf = new SimpleDateFormat("MM/dd/yyyy HH:mm:ss");
            String fileName;
            for (int i = 0; i < saveList.length; i++) {
                fileName = saveList[i].getName();
                    model[i] = fileName + ","
                            + sdf.format(saveList[i].lastModified());
            }

                saveComboBox.setModel(
                        new javax.swing.DefaultComboBoxModel(model));
        }
    }

